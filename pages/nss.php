            <p>
                National Service Scheme (NSS) is a Central Government Educational Programme started in the year 1969. It is functioning under the ministry of Youth Affairs and Sports at the national level and under the Ministry of Higher Education at the State level. The NSS has been introduced in colleges and universities all over India in response to the Kothari Commission’s recommendation that education should be oriented to the social needs by involving students in service to the community.
            </p>

            <p>
                College of Applied Science Mavelikkara has a committed N.S.S. wing. We have two National Service Scheme (NSS) units comprising 100 volunteers. Volunteers who undergo the prescribed course of training are awarded certificates from the University. This certificate gives weightage for higher studies and is an additional qualification for appointment in public services.
            </p>

