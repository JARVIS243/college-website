            <h3>MODE OF SELECTION - PG COURSES</h3>

            <h4>Preparation of RANK LIST</h4>

            <h5>For MSc Programmes</h5>

            <h6>Graduates in CBCSS Pattern (2013 admission onwards)</h6>

            <p>
                The candidates will be ranked on the basis of the CCPA(S) plus the CCPA of the core ie A=
            </p>

            <p>
                {(CCPA(S)* x Total Credits) + (CCPA of Concerned Core Course x Credit of Concerned Core Course)}/ (Total Credits + Credit of concerned core course) B=A*10 (*symbol for multiplication) B is then normalized to 1000
            </p>

            <h6>Graduates in CBCSS Pattern (Prior to 2013 admission)</h6>

            <p>
                The candidates will be ranked on the basis of the CGPA(S)* plus the CGPA of the core ie A= {(CGPA(S) x Total Credits) + (CGPA of Concerned Core Course x Credit of Concerned Core Course)}/ (Total Credits + Credit of concerned core course) B=A*25 (*symbol for multiplication) B is then normalized to 1000.
            </p>

            <h6>Graduates in Annual Scheme/Other Pattern</h6>

            <p>
                The candidates will be ranked on the basis of total marks obtained for the optional subjects plus the marks obtained in the Optional Main subject. {Part III Total Marks (Out of 1000) + Marks obtained in concerned Main (Out of 600) / 1600} * 1000 In the case of candidates who have qualified under three main systems the marks obtained in the three main plus the marks obtained in the main concerned will be used for ranking.
            </p>

            <h5>MCom Programme</h5>

            <h6>Graduates in CBCSS Pattern (2013 admission onwards)</h6>

            <p>
                Candidates qualified in CBCS pattern will be ranked on the basis of the CCPA obtained in the Core and Complementary Courses. A= {(CCPA(core course) x Core course Credit) + (CCPA(Complementary course ) x Complementary course Credit) } / (Total Credit for core + Total Credit for complementary ) B=A*10 B is then normalized to 1000.
            </p>

            <h6>Graduates in CBCSS Pattern (Prior to 2013 admission)</h6>

            <p>
                Candidates qualified in CBCS pattern will be ranked on the basis of the CGPA obtained in the Core and Complementary Courses. A= {(CGPA(core course) x Core course Credit) + (CGPA (Complementary course) x Complementary course Credit) } / (Total Credit for core + Total Credit for complementary ) B=A*25 B is then normalized to 1000.
            </p>

            <h6>Graduates in Annual Scheme/Other Pattern</h6>

            <p>
                The internal marks awarded at the B.Com level will not be considered while calculating index marks. NOTE: In case of the candidates who have passed the Vocational/ Restructured the marks scored in the University Examination (2nd, 4th and 6th semesters) alone will be considered for ranking purpose
            </p>

            <p>
                NOTE: Weightage/Deduction of marks while calculating the index marks and resolving the tie will be as per the norms prescribed by the university from time to time.
            </p>

