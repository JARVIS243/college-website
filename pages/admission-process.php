            <h3>ELIGIBILITY - PG Courses</h3>

            <h4>ELIGIBILITY FOR ADMISSION</h4>

            <p>
                Eligibility for admission is as specified by the University of Kerala for admission to each Programme.
            </p>

            <h5>Nativity</h5>

            <p>
                Candidate should be an Indian citizen. Candidates seeking admission to PG courses will be categorized as Keralite and Non-Keralite.
            </p>

            <p>
                <strong>Keralite:</strong> A candidate of Kerala origin will be categorized as a Keralite. Children of All India Service Officers allotted to Kerala Cadre are deemed to be Keralites as per G.O. (Rt) No.822/08/H.Edn Dated 09/05/2008. But they will not be eligible for communal/special reservation.
            </p>

            <p>
                <strong>Non-Keralite:</strong> A candidate who does not come under Keralite category will be categorized as Non-Keralite. Such a candidate will be eligible for admission only after all the applicants of Keralite origin are ranked and allotted.
            </p>

            <h5>Certificates Required for Nativity Proof</h5>

            <ul>
                <li>
                    In the case of candidates who have undergone College studies in Kerala, a Course Certificate from the Head of the educational institution last attended in Kerala, certifying that the candidate has undergone his/her studies in Kerala for not less than 3 (three) years within a continuous period of 12 (twelve) years, immediately prior to the completion of the Bachelor’s Degree or equivalent examination.
                </li>

                <li>
                    A certificate from the Village Officer/Tahsildar to show that he/she or his/her mother/father was born in Kerala.
                </li>

                <li>
                    Certificate from the Village Officer/Tahsildar to the effect that the candidate has been a resident of Kerala State for a period of five years within a continuous period of twelve years.
                </li>

                <li>
                    The true copy of relevant page of Secondary School Leaving Certificate showing the Place of Birth in Kerala of the candidate.
                </li>

                <li>
                    The true copy of the relevant page of the Secondary School Leaving Certificate showing Place of Birth in Kerala of either of the parents of the candidate with corroborative certificate to establish the relationship between the parent and the candidate.
                </li>

                <li>
                    The true copy of the relevant page of the Passport issued by the Government of India of the candidate, showing Place of Birth in Kerala or of either of the parents of the candidate showing Place of Birth in Kerala with corroborative certificate to establish the relationship between the parent and the candidate.
                </li>
            </ul>

            <p>
                Non-Keralites should furnish a copy of the Certificate of Nativity from the revenue authority or local body concerned.
            </p>

