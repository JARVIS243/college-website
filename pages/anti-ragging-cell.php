            <h2>ANTI RAGGING ACT</h2>

            <h4>RAGGING ACT. 1998.</h4>

            <p>
                The institution will take stern action against the offenders.
                The cardinal points contained in the act are furnished below for
                information of the concerned.
            </p>

            <h5>(i) SHORT TITLE AND EXTENT</h5>

            <p>
                (a) The Act may be called the Kerala Prohibition of Ragging Act 1998.<br>
                (b) It extends to the whole of the State of Kerala.
            </p>

            <h5>(ii) DEFINITION</h5>

            <p>
                “Ragging ” means display of disorderly conduct, doing any act which
                causes or is likely to cause physical or psychological harm or raise
                apprehension or fear or shame or embarrassment to a student in any,
                educational institution and includes
            </p>

            <p>
                a) teasing, abusing of, playing practical jokes on or causing hurt to
                such student; or
            </p>

            <p>
                b) asking the student to do any actor perform something which such
                student will not in the ordinary course willingly do.
            </p>

            <h5>(iii) PROHIBITION OF RAGGING</h5>

            <p>
                Ragging within or outside any educational institution is prohibited.
            </p>

            <h5>(iv) PENALTY FOR RAGGING</h5>

            <p>
                Whoever directly or indirectly commits, participates in, abets or
                propagates ragging within, or outside, any educational institution,
                shall, on conviction, be punished with imprisonment for a term which
                may extent to two years and shall also be liable to a fine which may
                extend to ten thousand rupees.
            </p>

            <h5>(v) DISMISSAL OF STUDENT</h5>

            <p>
                Any student convicted of an offense under section 4 shall also be
                dismissed from the educational institution and such student shall not
                be admitted in any other educational institution for a period of three
                years from the date of order of such dismissal.
            </p>

            <h5>(vi) SUSPENSION OF STUDENT</h5>

            <p>
                Whenever any student or, as the case may be, the parent or guardian
                or a teacher of an educational institution complains, in writing, of
                ragging to the head of the educational institution, the head of that
                educational institution shall, without prejudice to the foregoing
                provisions, within seven days of the receipt of the complaint,
                enquire into the matter mentioned in the complaint and, if,
                prima facie, it is found true, suspend the student who is accused
                of the offence, and shall, immediately, forward the complaint to
                the police station having jurisdiction over the area in which the
                educational institution is situated for further action.
            </p>

            <p>
                Where, on enquiry by the head of the educational institution, it is
                proved that there is no substance prima facie in the complaint
                received under sub-section (1), he shall intimate the fact, in
                writing, to the complainant.
            </p>

            <p>
                Copy of the prohibition of Ragging act 1998 will be available in the
                institution for reference. The applicants as well as his/her
                parent/guardian are required to furnish declaration on the awareness
                of the provisions of the Act in the application form.
            </p>

